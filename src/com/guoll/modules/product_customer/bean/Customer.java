package com.guoll.modules.product_customer.bean;

import java.util.List;

import com.guoll.modules.framework.base.BaseModel;

/**
 * 客户类
 * @author htnm
 */
public class Customer extends BaseModel{
	private Integer id;
	private String customerName;
	private String uCUserID;
	private String productsMark;
	private String provinceSpell;
	private String postName;
	private List<Product> products;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getuCUserID() {
		return uCUserID;
	}
	public void setuCUserID(String uCUserID) {
		this.uCUserID = uCUserID;
	}
	public String getProductsMark() {
		return productsMark;
	}
	public void setProductsMark(String productsMark) {
		this.productsMark = productsMark;
	}
	public String getProvinceSpell() {
		return provinceSpell;
	}
	public void setProvinceSpell(String provinceSpell) {
		this.provinceSpell = provinceSpell;
	}
	public String getPostName() {
		return postName;
	}
	public void setPostName(String postName) {
		this.postName = postName;
	}
	public List<Product> getProducts() {
		return products;
	}
	public void setProducts(List<Product> products) {
		this.products = products;
	}
	@Override
	public String toString() {
		return "Customer [id=" + id + ", customerName=" + customerName + ", uCUserID=" + uCUserID + ", productsMark="
				+ productsMark + ", provinceSpell=" + provinceSpell + ", postName=" + postName + ", products="
				+ products + "]";
	}
	
}
