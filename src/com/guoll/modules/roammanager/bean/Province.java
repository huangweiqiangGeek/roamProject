package com.guoll.modules.roammanager.bean;

import com.guoll.modules.framework.base.BaseModel;

public class Province extends BaseModel{
	private Integer id;
	private String province;
	private String province_en;
	private String webservice_url;
	private Integer delete_flag;

	

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getProvince_en() {
		return province_en;
	}

	public void setProvince_en(String province_en) {
		this.province_en = province_en;
	}

	public String getWebservice_url() {
		return webservice_url;
	}

	public void setWebservice_url(String webservice_url) {
		this.webservice_url = webservice_url;
	}

	public Integer getDelete_flag() {
		return delete_flag;
	}

	public void setDelete_flag(Integer delete_flag) {
		this.delete_flag = delete_flag;
	}

}
