package com.guoll.modules.sysmanage.bean;

public class SysDic {

    private Integer dict_type;
    private String code;
    private String name;
    
    public Integer getDict_type() {
        return dict_type;
    }
    public void setDict_type(Integer dictType) {
        dict_type = dictType;
    }
    public String getCode() {
        return code;
    }
    public void setCode(String code) {
        this.code = code;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    
}
