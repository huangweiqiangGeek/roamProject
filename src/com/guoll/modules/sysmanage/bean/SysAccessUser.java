package com.guoll.modules.sysmanage.bean;

import com.guoll.modules.framework.base.BaseModel;

public class SysAccessUser extends BaseModel{

	private Integer id;//主键
	private Integer user_id;
	private String user_code;
	private String user_name;//
	private String mobile;//
	private String telephone;
	private String ip;//
	private String access_time;
	private Integer access_flag;//1：登陆状态，2：未登录，3：强制退出
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getUser_id() {
		return user_id;
	}
	public void setUser_id(Integer user_id) {
		this.user_id = user_id;
	}
	public String getUser_code() {
		return user_code;
	}
	public void setUser_code(String user_code) {
		this.user_code = user_code;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getTelephone() {
		return telephone;
	}
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getAccess_time() {
		return access_time;
	}
	public void setAccess_time(String access_time) {
		this.access_time = access_time;
	}
	public Integer getAccess_flag() {
		return access_flag;
	}
	public void setAccess_flag(Integer access_flag) {
		this.access_flag = access_flag;
	}
}