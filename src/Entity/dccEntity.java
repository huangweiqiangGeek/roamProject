package Entity;

public class dccEntity {
	private int code;
	private String request_name;
	private String respond_name;
	public dccEntity(){
		
	}
	public dccEntity(int code, String request_name, String respond_name){
		this.code = code;
		this.request_name = request_name;
		this.respond_name = respond_name;
	}
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getRequest_name() {
		return request_name;
	}
	public void setRequest_name(String request_name) {
		this.request_name = request_name;
	}
	public String getRespond_name() {
		return respond_name;
	}
	public void setRespond_name(String respond_name) {
		this.respond_name = respond_name;
	}
}
