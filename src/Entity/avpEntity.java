package Entity;

public class avpEntity {
	private int code;
	private String name;
	private String type;
	private String data;

	public avpEntity(){
		
	}
	public avpEntity(int code, String name, String type, String data){
		this.code = code;
		this.name = name;
		this.type = type;
		this.data = data;
	}
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	
}
